// Reads _research/products.json and writes a clean TS data file
// at src/data/products.ts that references the converted WebPs.
import { readFile, writeFile } from 'node:fs/promises';

const raw = JSON.parse(await readFile('_research/products.json', 'utf8'));

// Categories: keep order for nav
const CATS = [
  { slug: 'carpet',           service: 'carpet',           label: 'Carpet' },
  { slug: 'engineered-wood',  service: 'engineered-wood',  label: 'Engineered Wood' },
  { slug: 'laminate',         service: 'laminate',         label: 'Laminate' },
  { slug: 'tile',             service: 'tile',             label: 'Tile' },
  { slug: 'vinyl-glue',       service: 'vinyl-glue',       label: 'Vinyl Glue (LVT)' },
  { slug: 'vinyl-click',      service: 'vinyl-click',      label: 'Vinyl Click (LVP / SPC)' },
];

const products = raw.map((p) => ({
  slug: p.slug,
  name: p.name.replace(/\s+/g, ' ').trim(),
  category: p.category,
  service: p.service,
  image: `/images/products/${p.category}/${p.slug}.webp`,
}));

// Sort within each category alphabetically by name
products.sort((a, b) => {
  if (a.category !== b.category) {
    return CATS.findIndex((c) => c.slug === a.category) - CATS.findIndex((c) => c.slug === b.category);
  }
  return a.name.localeCompare(b.name);
});

const ts = `// AUTO-GENERATED by scripts/write-products-data.mjs. Re-run to refresh.
// Source: scraped from floorstoyouokc.com on ${new Date().toISOString().slice(0, 10)}.

export type ProductCategory = {
  slug: string;
  service: string;
  label: string;
};

export type Product = {
  slug: string;
  name: string;
  category: string;
  service: string;
  image: string;
};

export const productCategories: ProductCategory[] = ${JSON.stringify(CATS, null, 2)};

export const products: Product[] = ${JSON.stringify(products, null, 2)};

export const getProduct = (slug: string) => products.find((p) => p.slug === slug);
export const productsByCategory = (cat: string) => products.filter((p) => p.category === cat);
export const productsByService = (service: string) => products.filter((p) => p.service === service);
`;

await writeFile('src/data/products.ts', ts);
console.log(`Wrote src/data/products.ts with ${products.length} products.`);
